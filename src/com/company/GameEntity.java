package com.company;

public class GameEntity {
    private String naem,pover;
    private int hp,dm;

    public String getNaem() {
        return naem;
    }

    public void setNaem(String naem) {
        this.naem = naem;
    }

    public String getPover() {
        return pover;
    }

    public void setPover(String pover) {
        this.pover = pover;
    }

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public int getDm() {
        return dm;
    }

    public void setDm(int dm) {
        this.dm = dm;
    }

}
